import { join } from 'node:path';
import Handlebars from 'handlebars';
import type { ThemeBundle } from '~/theme/types.ts';

interface MissingThemeAssetReference {
  owner: string;
  path: string;
  file: string;
}

interface AssetReference {
  owner: string;
  path: string;
}

type AstRecord = Record<string, unknown>;

export function findMissingThemeAssetReferences(theme: ThemeBundle): MissingThemeAssetReference[] {
  const refs = new Map<string, AssetReference>();

  for (const [name, source] of Object.entries(theme.templates)) {
    collectSourceReferences(refs, `Theme template '${name}'`, source);
  }
  for (const [name, source] of Object.entries(theme.partials)) {
    collectSourceReferences(refs, `Theme partial '${name}'`, source);
  }

  const missing: MissingThemeAssetReference[] = [];
  for (const ref of refs.values()) {
    if (themeHasAsset(theme, ref.path)) continue;
    missing.push({
      owner: ref.owner,
      path: ref.path,
      file: expectedThemeAssetFile(theme, ref.path),
    });
  }

  return missing.sort((a, b) => {
    const owner = a.owner.localeCompare(b.owner);
    return owner !== 0 ? owner : a.path.localeCompare(b.path);
  });
}

export function formatMissingThemeAssetReference(ref: MissingThemeAssetReference): string {
  return `${ref.owner} references theme asset '${ref.path}' but ${ref.file} is missing on disk. Run the theme build step first when this file is generated by gulp, npm scripts, or another theme asset pipeline.`;
}

function collectSourceReferences(refs: Map<string, AssetReference>, owner: string, source: string) {
  let ast: unknown;
  try {
    ast = Handlebars.parse(source);
  } catch {
    return;
  }
  for (const path of collectLiteralAssetPaths(ast)) {
    const normalized = normalizeAssetPath(path);
    if (!normalized || shouldSkipAssetReference(normalized)) continue;
    refs.set(`${owner}\0${normalized}`, { owner, path: normalized });
  }
}

function collectLiteralAssetPaths(ast: unknown): string[] {
  const out: string[] = [];
  visitAst(ast, (node) => {
    if (!isAssetExpression(node)) return;
    const firstParam = getArrayField(node, 'params')[0];
    if (!isAstRecord(firstParam) || firstParam.type !== 'StringLiteral') return;
    if (typeof firstParam.value === 'string') out.push(firstParam.value);
  });
  return out;
}

function visitAst(node: unknown, visit: (node: AstRecord) => void): void {
  if (Array.isArray(node)) {
    for (const item of node) visitAst(item, visit);
    return;
  }
  if (!isAstRecord(node)) return;

  visit(node);
  for (const value of Object.values(node)) {
    if (value && typeof value === 'object') visitAst(value, visit);
  }
}

function isAssetExpression(node: AstRecord): boolean {
  const type = node.type;
  if (type !== 'MustacheStatement' && type !== 'SubExpression') return false;

  const path = node.path;
  if (!isAstRecord(path)) return false;
  return path.original === 'asset';
}

function getArrayField(node: AstRecord, key: string): unknown[] {
  const value = node[key];
  return Array.isArray(value) ? value : [];
}

function isAstRecord(value: unknown): value is AstRecord {
  return value !== null && typeof value === 'object' && !Array.isArray(value);
}

function normalizeAssetPath(path: string): string {
  return path.replace(/^\//, '');
}

function shouldSkipAssetReference(path: string): boolean {
  return /^[a-z][a-z0-9+.-]*:/i.test(path) || path.startsWith('//');
}

function themeHasAsset(theme: ThemeBundle, path: string): boolean {
  const candidates = [path, `assets/${path}`];
  return candidates.some((candidate) => theme.assets.has(candidate));
}

function expectedThemeAssetFile(theme: ThemeBundle, path: string): string {
  if (path.startsWith('assets/')) return join(theme.rootDir, path);
  return join(theme.rootDir, 'assets', path);
}
