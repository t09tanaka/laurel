import { writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { ensureDir } from '~/util/fs.ts';
import { type HeadersConfig, validateHstsForPreload } from './headers.ts';
import type { PrecompressFormat } from './precompress.ts';
import { type RedirectRule, collapseRedirects } from './redirects.ts';

const CATCH_ALL = '/*';

const SECURITY_HEADER_FIELDS: ReadonlyArray<{
  key: keyof Omit<HeadersConfig['security'], 'custom'>;
  name: string;
}> = [
  { key: 'content_type_options', name: 'X-Content-Type-Options' },
  { key: 'frame_options', name: 'X-Frame-Options' },
  { key: 'referrer_policy', name: 'Referrer-Policy' },
  { key: 'strict_transport_security', name: 'Strict-Transport-Security' },
  { key: 'content_security_policy', name: 'Content-Security-Policy' },
  { key: 'permissions_policy', name: 'Permissions-Policy' },
  { key: 'cross_origin_opener_policy', name: 'Cross-Origin-Opener-Policy' },
  { key: 'cross_origin_embedder_policy', name: 'Cross-Origin-Embedder-Policy' },
];

interface HeaderEntry {
  name: string;
  value: string;
}

interface CacheRule {
  pattern: string;
  cacheControl: string;
}

export function toApacheRewritePattern(pattern: string): string {
  const path = pattern.startsWith('/') ? pattern.slice(1) : pattern;
  if (path === '') return '^$';
  let out = '';
  for (const ch of path) {
    if (ch === '*') {
      out += '(.*)';
    } else {
      out += ch.replace(/[\\^$+?.()|[\]{}]/g, '\\$&');
    }
  }
  return `^${out}$`;
}

export function buildApacheHtaccess(opts: {
  headers: HeadersConfig;
  rules: readonly RedirectRule[];
  // Which `AddEncoding` mappings to emit, mirroring `[build].precompress`. The
  // `mod_brotli` / `mod_deflate` `<IfModule>` guards already make these inert
  // when the module is absent, but emitting a mapping for a format Laurel never
  // produces is misleading. Defaults to `both` for callers that omit it.
  precompress?: PrecompressFormat;
}): string {
  const redirects = collapseRedirects(opts.rules);
  const cacheRules = collectCacheRules(opts.headers);
  const securityHeaders = collectSecurityHeaders(opts.headers.security);
  const lines: string[] = [];

  lines.push('# Generated by Laurel from redirects.yaml + deploy.headers.');
  lines.push('# Requires AllowOverride FileInfo and Apache modules: mod_rewrite, mod_headers.');
  lines.push('');
  lines.push('DirectoryIndex index.html');
  lines.push('ErrorDocument 404 /404.html');
  lines.push('');
  lines.push('FileETag MTime Size');
  lines.push('');
  lines.push('AddType text/css .css');
  lines.push('AddType application/javascript .js .mjs');
  lines.push('AddType image/svg+xml .svg');
  lines.push('AddType image/webp .webp');
  lines.push('AddType image/avif .avif');
  lines.push('AddType font/woff .woff');
  lines.push('AddType font/woff2 .woff2');
  const format = opts.precompress ?? 'both';
  if (format === 'brotli' || format === 'both') {
    lines.push('');
    lines.push('<IfModule mod_brotli.c>');
    lines.push('  AddEncoding br .br');
    lines.push('</IfModule>');
  }
  if (format === 'gzip' || format === 'both') {
    lines.push('');
    lines.push('<IfModule mod_deflate.c>');
    lines.push('  AddEncoding gzip .gz');
    lines.push('</IfModule>');
  }

  lines.push('');
  lines.push('<IfModule mod_rewrite.c>');
  lines.push('  RewriteEngine On');

  if (redirects.length > 0) {
    lines.push('');
    lines.push('  # Redirects (from redirects.yaml; first-match)');
    for (const rule of redirects) {
      lines.push(
        `  RewriteRule ${toApacheRewritePattern(rule.from)} ${rule.to} [R=${rule.status},L]`,
      );
    }
  }

  if (cacheRules.length > 0) {
    lines.push('');
    lines.push('  # Cache rule markers consumed by mod_headers below.');
    cacheRules.forEach((rule, index) => {
      lines.push('  RewriteCond %{ENV:LAUREL_CACHE_MATCHED} !1');
      lines.push(
        `  RewriteRule ${toApacheRewritePattern(rule.pattern)} - [E=LAUREL_CACHE_${index}:1,E=LAUREL_CACHE_MATCHED:1]`,
      );
    });
  }

  lines.push('');
  lines.push("  # Resolve Laurel's slug/index.html output for clean URLs.");
  lines.push('  RewriteCond %{REQUEST_FILENAME} !-f');
  lines.push('  RewriteCond %{REQUEST_FILENAME}/index.html -f');
  lines.push('  RewriteRule ^(.+[^/])$ $1/index.html [L]');
  lines.push('');
  lines.push('  RewriteCond %{REQUEST_FILENAME} !-f');
  lines.push('  RewriteCond %{REQUEST_FILENAME}index.html -f');
  lines.push('  RewriteRule ^(.+)/$ $1/index.html [L]');
  lines.push('</IfModule>');

  if (cacheRules.length > 0 || securityHeaders.length > 0) {
    lines.push('');
    lines.push('<IfModule mod_headers.c>');
    cacheRules.forEach((rule, index) => {
      lines.push(
        `  Header set Cache-Control "${escapeApacheValue(rule.cacheControl)}" env=LAUREL_CACHE_${index}`,
      );
    });
    for (const header of securityHeaders) {
      lines.push(`  Header always set ${header.name} "${escapeApacheValue(header.value)}"`);
    }
    lines.push('</IfModule>');
  }

  return `${lines.join('\n')}\n`;
}

function collectSecurityHeaders(security: HeadersConfig['security']): HeaderEntry[] {
  const entries: HeaderEntry[] = [];
  for (const { key, name } of SECURITY_HEADER_FIELDS) {
    const value = security[key];
    if (typeof value === 'string' && value.length > 0) {
      entries.push({
        name,
        value: name === 'Strict-Transport-Security' ? validateHstsForPreload(value) : value,
      });
    }
  }
  for (const [name, value] of Object.entries(security.custom)) {
    if (typeof value === 'string' && value.length > 0) {
      entries.push({ name, value });
    }
  }
  return entries;
}

function collectCacheRules(headers: HeadersConfig): CacheRule[] {
  const seen = new Set<string>();
  const ordered: CacheRule[] = [];
  let catchAll: CacheRule | null = null;

  for (const rule of headers.cache_rules) {
    if (seen.has(rule.pattern)) continue;
    seen.add(rule.pattern);
    const entry = { pattern: rule.pattern, cacheControl: rule.cache_control };
    if (rule.pattern === CATCH_ALL) {
      catchAll = entry;
    } else {
      ordered.push(entry);
    }
  }

  if (catchAll) ordered.push(catchAll);
  return ordered;
}

function escapeApacheValue(value: string): string {
  return value.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

export async function emitApacheHtaccess(opts: {
  outputDir: string;
  enabled: boolean;
  headers: HeadersConfig;
  rules: readonly RedirectRule[];
  precompress?: PrecompressFormat;
}): Promise<void> {
  if (!opts.enabled) return;
  await ensureDir(opts.outputDir);
  await writeFile(
    join(opts.outputDir, '.htaccess'),
    buildApacheHtaccess({
      headers: opts.headers,
      rules: opts.rules,
      precompress: opts.precompress,
    }),
  );
}
