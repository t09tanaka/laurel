import { writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { ensureDir } from '~/util/fs.ts';
import type { HeadersConfig } from './headers.ts';
import { type RedirectRule, collapseRedirects } from './redirects.ts';

// Caddy consumes a single Caddyfile for static serving, headers, redirects, and
// fallback rewrites. Laurel writes this under `.laurel/` for the same reason as
// the nginx snippet: it is an operator include/copy artifact, not public site
// content.

const CATCH_ALL = '/*';

const SECURITY_HEADER_FIELDS: ReadonlyArray<{
  key: keyof Omit<HeadersConfig['security'], 'custom'>;
  name: string;
}> = [
  { key: 'content_type_options', name: 'X-Content-Type-Options' },
  { key: 'frame_options', name: 'X-Frame-Options' },
  { key: 'referrer_policy', name: 'Referrer-Policy' },
  { key: 'strict_transport_security', name: 'Strict-Transport-Security' },
  { key: 'content_security_policy', name: 'Content-Security-Policy' },
  { key: 'permissions_policy', name: 'Permissions-Policy' },
  { key: 'cross_origin_opener_policy', name: 'Cross-Origin-Opener-Policy' },
  { key: 'cross_origin_embedder_policy', name: 'Cross-Origin-Embedder-Policy' },
];

interface HeaderEntry {
  name: string;
  value: string;
}

interface CacheRule {
  pattern: string;
  cacheControl: string;
}

interface BuildCaddyfileOptions {
  headers: HeadersConfig;
  rules: readonly RedirectRule[];
  // Filesystem root Caddy serves from. Defaults to the same self-hosted path as
  // the nginx emitter so rsync/VPS guides can switch servers without changing
  // their artifact layout.
  root?: string;
  // Caddy site address, e.g. `example.com` for automatic HTTPS or `:80` for a
  // plain HTTP/include-friendly default.
  siteAddress?: string;
}

export function buildCaddyfile(opts: BuildCaddyfileOptions): string {
  const root = opts.root ?? '/var/www/laurel';
  const siteAddress = opts.siteAddress ?? ':80';
  const cacheRules = collectCacheRules(opts.headers);
  const securityHeaders = collectSecurityHeaders(opts.headers.security);
  const redirects = collapseRedirects(opts.rules);

  const lines: string[] = [];
  lines.push('# Generated by Laurel from redirects.yaml + deploy.headers.');
  lines.push(`# Copy or import this file from ${root}/.laurel/Caddyfile.`);
  lines.push(`${siteAddress} {`);
  lines.push(`    root * ${root}`);
  lines.push('    encode zstd gzip');

  if (redirects.length > 0) {
    lines.push('');
    lines.push('    # Redirects (from redirects.yaml; first-match)');
    redirects.forEach((r, i) => {
      lines.push(`    @redirect_${i} path ${toCaddyPathPattern(r.from)}`);
      lines.push(`    redir @redirect_${i} ${r.to} ${r.status}`);
    });
  }

  if (cacheRules.length > 0) {
    lines.push('');
    lines.push('    # Cache headers (from deploy.headers.cache_rules)');
    cacheRules.forEach((rule, i) => {
      lines.push(`    @cache_${i} path ${toCaddyPathPattern(rule.pattern)}`);
      lines.push(`    header @cache_${i} Cache-Control "${escapeCaddyValue(rule.cacheControl)}"`);
    });
  }

  if (securityHeaders.length > 0) {
    lines.push('');
    lines.push('    # Security headers (from deploy.headers.security)');
    lines.push('    header {');
    for (const h of securityHeaders) {
      lines.push(`        ${h.name} "${escapeCaddyValue(h.value)}"`);
    }
    lines.push('    }');
  }

  lines.push('');
  lines.push('    try_files {path} {path}/index.html =404');
  lines.push('    file_server {');
  lines.push('        precompressed br gzip');
  lines.push('    }');
  lines.push('');
  lines.push('    handle_errors {');
  lines.push('        rewrite * /404.html');
  lines.push('        file_server');
  lines.push('    }');
  lines.push('}');
  return `${lines.join('\n')}\n`;
}

function collectSecurityHeaders(security: HeadersConfig['security']): HeaderEntry[] {
  const entries: HeaderEntry[] = [];
  for (const { key, name } of SECURITY_HEADER_FIELDS) {
    const value = security[key];
    if (typeof value === 'string' && value.length > 0) {
      entries.push({ name, value });
    }
  }
  for (const [name, value] of Object.entries(security.custom)) {
    if (typeof value === 'string' && value.length > 0) {
      entries.push({ name, value });
    }
  }
  return entries;
}

function collectCacheRules(headers: HeadersConfig): CacheRule[] {
  const seen = new Set<string>();
  const ordered: CacheRule[] = [];
  let catchAll: CacheRule | null = null;

  for (const rule of headers.cache_rules) {
    if (seen.has(rule.pattern)) continue;
    seen.add(rule.pattern);
    const entry: CacheRule = {
      pattern: rule.pattern,
      cacheControl: rule.cache_control,
    };
    if (rule.pattern === CATCH_ALL) {
      catchAll = entry;
    } else {
      ordered.push(entry);
    }
  }

  if (catchAll) ordered.push(catchAll);
  return ordered;
}

function toCaddyPathPattern(pattern: string): string {
  return pattern === CATCH_ALL ? '*' : pattern;
}

function escapeCaddyValue(value: string): string {
  return value.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

export async function emitCaddyfile(opts: {
  outputDir: string;
  enabled: boolean;
  headers: HeadersConfig;
  rules: readonly RedirectRule[];
  root?: string;
  siteAddress?: string;
}): Promise<void> {
  if (!opts.enabled) return;
  const body = buildCaddyfile({
    headers: opts.headers,
    rules: opts.rules,
    root: opts.root,
    siteAddress: opts.siteAddress,
  });
  const targetDir = join(opts.outputDir, '.laurel');
  await ensureDir(targetDir);
  await writeFile(join(targetDir, 'Caddyfile'), body);
}
